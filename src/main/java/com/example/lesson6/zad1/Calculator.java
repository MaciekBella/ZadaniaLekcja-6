package com.example.lesson6.zad1;

public class Calculator {

    public static int sum(int numbers1, int numbers2) {
        return numbers1 + numbers2;
    }

    public static int subtract(int numbers1, int numbers2) {
        return numbers1 - numbers2;
    }

    public static int multiply(int numbers1, int numbers2) {
        return numbers1 * numbers2;
    }

    public static int division(int numbers1, int numbers2) {
        return numbers1 / numbers2;
    }
}
